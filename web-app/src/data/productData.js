// ─── Helper Functions ───

export function getProductById(id) {
    return allProducts.find((p) => p.id === Number(id));
}

export function getSimilarProducts(productId, limit = 4) {
    const product = getProductById(productId);
    if (!product) return allProducts.slice(0, limit);

    // Prioritize same category, then other products
    const sameCategory = allProducts.filter(
        (p) => p.id !== product.id && p.category === product.category
    );
    const others = allProducts.filter(
        (p) => p.id !== product.id && p.category !== product.category
    );
    return [...sameCategory, ...others].slice(0, limit);
}

export function getDealProducts() {
    return allProducts.filter((p) => p.source === 'deals');
}

export function getSellingProducts() {
    return allProducts.filter((p) => p.source === 'selling');
}

export function getDropshipProducts() {
    return allProducts.filter((p) => p.source === 'dropship');
}

